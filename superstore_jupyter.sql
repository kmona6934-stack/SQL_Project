create view Customers_Levels As
select [Customer Name],round(sum(Sales),0) as Total_Sales, 
case
when sum(Sales)>10000 then 'VIP Customer'
when sum(Sales)>5000 then 'Medium Customer'
else 'Normal Customer'
end as Customer_Levels
from Superstore_Data
group by [Customer Name];


select Customer_Levels,count(Customer_Levels) as Customer_Count
from Customers_Levels
group by Customer_Levels
order by count(Customer_Levels);


